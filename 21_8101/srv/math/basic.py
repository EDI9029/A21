def addUp(originNum, plus):
    return originNum + plus

def subtract(originNum, minus):
    return originNum - minus

def multiply(originNum, times):
    return originNum * times

def division(originNum, divisor):
    return ((originNum  / divisor) * 100)/100

def remainder(originNum, divisor):
    return ((originNum  % divisor) * 100)/100

